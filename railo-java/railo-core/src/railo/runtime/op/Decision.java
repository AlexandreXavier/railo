    public static boolean isDate(String str,Locale locale, TimeZone tz,boolean lenient) {
    	str=str.trim();
    	tz=ThreadLocalPageContext.getTimeZone(tz);
    	DateFormat[] df;

    	// get Calendar
        Calendar c=(Calendar) Caster.calendarsMap.get(locale);
        if(c == null) {
            c=Calendar.getInstance(locale);
            Caster.calendarsMap.put(locale,c);
        }
        synchronized(c){
	        // datetime
	        df=FormatUtil.getDateTimeFormats(locale,false);//dfc[FORMATS_DATE_TIME];
	    	for(int i=0;i<df.length;i++) {
	            try {
	            	synchronized(c) {
		            	df[i].parse(str);
		            	return true;
	            	}
	            }
	            catch (ParseException e) {}
	        }
	        // date
	        df=FormatUtil.getDateFormats(locale,false);//dfc[FORMATS_DATE];
	    	for(int i=0;i<df.length;i++) {
	            try {
	            	df[i].setTimeZone(tz);
	            	synchronized(c) {
		            	df[i].parse(str);
		            	return true;
	            	}
	        }
	            catch (ParseException e) {}
	        }
	    	
	        // time
	        df=FormatUtil.getTimeFormats(locale,false);//dfc[FORMATS_TIME];
	        for(int i=0;i<df.length;i++) {
	            try {
	            	df[i].setTimeZone(tz);
	            	synchronized(c) {
		            	df[i].parse(str);
		            	return true;
	            	}
	            } 
	            catch (ParseException e) {}
	        }
        } 
        if(lenient) return isDateSimple(str, false);
        return false;
    }
