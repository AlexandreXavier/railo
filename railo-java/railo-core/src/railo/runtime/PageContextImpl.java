	/**
	 * @see javax.servlet.jsp.PageContext#release()
	 */
	public void release() {
		parent=null;
		// Attention have to be before close
		if(client!=null){
        	client.release();
        	client=null;
        }
        
		
		close();
        thread=null;
        
		// Scopes
        if(hasFamily) {
        	request=null;
        	_url=null;
        	_form=null;
            urlForm=null;
            undefined=null;
            variables=null;
            variablesRoot=null;
            if(threads!=null && threads.size()>0) threads.clear();
            
        }
        else {
            variables=variablesRoot;
            variables.release();
            undefined.release();
            urlForm.release();
        	request.release();
        }
        cgi.release();
        argument.release();
        local=localUnsupportedScope;
        
        cookie.release();
		//if(cluster!=null)cluster.release();
        
        //client=null;
        session=null;
		application=null;
		
		// Properties
        requestTimeout=-1;
        outputState=0;
        cfid=null;
        cftoken=null;
        locale=null;
        timeZone=null;
        
        if(config.debug()) debugger.reset();
        
        // Pools
        errorPagePool.clear();

        // transaction connection
        if(!conns.isEmpty()){
        	java.util.Iterator it = conns.entrySet().iterator();
        	DatasourceConnectionPool pool = config.getDatasourceConnectionPool();
        	while(it.hasNext())	{
        		//print.out("releasee");
        		pool.releaseDatasourceConnection((DatasourceConnection)(((Map.Entry)it.next()).getValue()));
        	}
        	conns.clear();
        }
        
        
        
        pathList.clear();
        includePathList.clear();
		executionTime=0;
		
		bodyContentStack.release();
		

        
		//activeComponent=null;
		remoteUser=null;
		exception=null;
		ftpPool.clear();
        parentTag=null;
        currentTag=null;
        
        // Req/Rsp
        //if(req!=null)
        	req.clear();
        req=null;
        rsp=null;
        servlet=null;

        // Writer
        writer=null;
        forceWriter=null;
        
	}
