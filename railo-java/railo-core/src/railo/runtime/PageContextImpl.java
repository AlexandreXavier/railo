    /**
     * initialize the cfid and the cftoken
     */
    private void initIdAndToken() {
        boolean setCookie=true;
        // From URL
        Object oCfid = urlScope().get(ClientSupport.CFID,null);
        Object oCftoken = urlScope().get(ClientSupport.CFTOKEN,null);
        // Cookie
        if((oCfid==null || !Decision.isGUIdSimple(oCfid)) || oCftoken==null) {
            setCookie=false;
            oCfid = cookieScope().get(ClientSupport.CFID,null);
            oCftoken = cookieScope().get(ClientSupport.CFTOKEN,null);
        }
        if(oCfid!=null && !Decision.isGUIdSimple(oCfid) ) {
        	oCfid=null;
        }
        // New One
        if(oCfid==null || oCftoken==null) {
            setCookie=true;
            cfid=ScopeContext.getNewCFId();
            cftoken=ScopeContext.getNewCFToken();
        }
        else {
            cfid=Caster.toString(oCfid,null);
            cftoken=Caster.toString(oCftoken,null);
        }
        
        if(setCookie && applicationContext.isSetClientCookies()) {
            cookieScope().setCookieEL("cfid",cfid,-1,false,"/",applicationContext.isSetDomainCookies()?(String) cgiScope().get("server_name",null):null);
            cookieScope().setCookieEL("cftoken",cftoken,-1,false,"/",applicationContext.isSetDomainCookies()?(String) cgiScope().get("server_name",null):null);
        }
    }

